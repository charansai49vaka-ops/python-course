# if we have to print A number we can able to easily print it
# but if we want to print 1 to 100 numbers we can't be able to do it easy 
# to perform this task easily we use for loop 
# where we can able print any n number of number using one line of code
# in for loop if we want to print 10 numbers we should have to give it as(1 , 11)
# it follows (n , n-1) condition

for i in range(1 , 11):
    print(i)    #we can able to print 1 to 10
for j in range(1, 11):
    print(5*(j))   # we can able to print 5 table numbers like 5,10,...
for z in range(1 , 11):
    print("5 x",z,"=",(5*z))     #we can able to print 5 table fully
# like 5x1=5.....
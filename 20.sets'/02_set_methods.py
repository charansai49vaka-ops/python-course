# there are different types of set methods some of them are
# add--add the values to the set
# remove--removes the value to from the set
# discard--discard are the method which is used when we don't know what value is present in the set but to print without getting error
# pop--removes a value from the set as they want

set={1,3,4,5}
set.add(2)
set.remove(5)
set.discard(12)
set.pop()
print(set)
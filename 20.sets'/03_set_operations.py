# there are somay number of operations in the set the manily used operations are:
'''union -- print all the numbers present in the two sets with no repetitions
intersection--print only same numbers present in the two set
difference--print the different numbers present in the two sets'''
a={23,43,4,3}
b={3,5,6,4,}
print(a.union(b))
print(a.intersection(b))
print(a.difference(b))



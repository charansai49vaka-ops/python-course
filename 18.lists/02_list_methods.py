 # we know that list is one of the 4 built-in data type present in python 
# but lists also contains a particular methods to excecute 
sai=[1,3,5]
sai.append(7) #7 will be added to the last of the list
sai.insert( 1, 2)  #here 1 shows the index where 2 should add
sai.remove(3) # It will remove 3 from the list
sai.pop() # It removes the lost number in the list
sai.reverse() # It will reverse the order of the list like abcd to dcba
sai.sort() # it will sort the numbers in the list as ascending order (small - big)
print(sai)